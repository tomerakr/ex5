package hac.beans;

/**
 * The Utility class provides utility constants for the Wordle game.
 * It defines values for different letter states.
 */
public class Utility {

    /**
     * Represents the value for a wrong letter.
     */
    public static final int WRONG_LETTER = -1;

    /**
     * Represents the value for a right letter but wrong place.
     */
    public static final int RIGHT_LETTER = 0;

    /**
     * Represents the value for a right letter and right place.
     */
    public static final int RIGHT_LETTER_AND_PLACE = 1;
}

